#include "testApp.h"

///////////////////////////////////////////////////////////////////////////////////
// setup --------------------------------------------------------------------------
///////////////////////////////////////////////////////////////////////////////////
void testApp::setup(){	
	ofSetVerticalSync(true);
	ofBackground(0, 0, 0);
	ofSetFullscreen(true);
	
	capture.initGrabber(640,480);
	
	cm.setup(640, 480);
	cmSettings.setup(&cm);
}

///////////////////////////////////////////////////////////////////////////////////
// update -------------------------------------------------------------------------
///////////////////////////////////////////////////////////////////////////////////
void testApp::update(){
	capture.grabFrame();
	
	if (capture.isFrameNew())
		cm.update(capture.getPixels());	
	
}
///////////////////////////////////////////////////////////////////////////////////
// draw ---------------------------------------------------------------------------
///////////////////////////////////////////////////////////////////////////////////
void testApp::draw(){
	if(cm.isReady())
		cm.draw(0, 0, ofGetWidth(), ofGetHeight());
	cmSettings.draw(0,0);
}
///////////////////////////////////////////////////////////////////////////////////
// keyPressed ---------------------------------------------------------------------
///////////////////////////////////////////////////////////////////////////////////
void testApp::keyPressed(int key){
	cmSettings.keyPressed(key);
	
	if (key==' ')
		ofToggleFullscreen();
}
